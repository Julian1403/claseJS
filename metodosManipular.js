//Metodos para manipulacion de arrays

//.map() -> crea un nuevo array con base al array de entrada, sin mutar el array inicial, permitiendo manipular los datos en el array nuevo.
//.pop() -> eliminar el ultimo elemento de un array
//.push() -> añadir uno o mas elementos a un array existente
//.shift() -> eliminar el primer elemento de un array
//.unshift() -> agregar elementos al inicio de un array
//.slice() -> devuelve una copia de una parte el array inicial, solo que aqui­ en este escenario le doy un parametro de entrada y uno de salida
//.some() -> comprueba si al menos un elemento del array inicial cumple con una condicion.
//.splice() -> manipula el contenido de un array, eliminando o agregando elementos , primer dato de entrada posicion donde iniciara a trabajar, segundo parametro cantidad de registros a eliminar, tercer dato de entrada valor a insertar.
//.filter() -> crea un nuevo array con todos los elementos siempre y cuando cumplan una condicion
//.find() -> devuelve directamente el valor del primer elemento que cumpla la condicion
//.indexOf() -> permite retornar el primer Ã­ndice en el que se pueda cumplir una condicion
//.every() -> comprueba si cada uno de los elementos del array cumplen una funcion
//.includes() -> determina si en un array hay un determinado elemento o condicion y devuelve true o false
//.join() -> este metodo se encarga de unir uno o mas elementos de un array
//.sort() -> se encarga de ordenar los elementos de un array, devuelve un arreglo nuevo con los datos ordenados.
//.reduce() -> ejecuta una funcion reductora sobre cada uno de los elementos del array.